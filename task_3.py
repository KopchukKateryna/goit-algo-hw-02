# У багатьох мовах програмування ми маємо справу з виразами,
# виділеними символами-розділювачами, такими як круглі ( ), квадратні [ ]
# або фігурні дужки { }.

# Напишіть програму, яка читає рядок з послідовністю символів-розділювачів,
# наприклад, ( ) { [ ] ( ) ( ) { } } }, і надає відповідне повідомлення,
# коли розділювачі симетричні, несиметричні, наприклад ( ( ( ) , або
# коли розділювачі різних видів стоять у парі, як-от ( }.


def check_delimiters(sequence):
    """
    Перевіряє симетрію символів-розділювачів у рядку.

    Параметри:
        sequence (str): Рядок з послідовністю символів-розділювачів.

    Повертає:
        str: "Симетрично", якщо розділювачі симетричні, або "Несиметрично" в іншому випадку.
    """
    stack = []
    opening = "({["
    closing = ")}]"
    pairs = {")": "(", "}": "{", "]": "["}

    for char in sequence:
        if char in opening:
            stack.append(char)
        elif char in closing:
            if stack and stack[-1] == pairs[char]:
                stack.pop()
            else:
                return "Несиметрично"
        else:
            continue
    if stack:
        return "Несиметрично"
    else:
        return "Симетрично"


test_cases = [
    "( ){[ 1 ]( 1 + 3 )( ){ }}",  # Симетрично
    "( 23 ( 2 - 3);",  # Несиметрично
    "( 11 }",  # Несиметрично
    "",  # Симетрично (порожній рядок)
    "No delimiters here!",  # Симетрично
    "([{}])",  # Симетрично
    "{[()]}",  # Симетрично
    "{[(])}",  # Несиметрично
    "(((())))",  # Симетрично
    "(((()))",  # Несиметрично
    "(()))",  # Несиметрично
    "((((",  # Несиметрично
    "))))",  # Несиметрично
    "{[()()]}",  # Симетрично
    "{[({})]}",  # Симетрично
    "{[({})]}]",  # Несиметрично
    "function() { return [1, 2, 3]; }",  # Симетрично
    "if (x > y) { do_something();",  # Несиметрично
    "[{()}({[]})]",  # Симетрично
    "([)]",  # Несиметрично
    "((2 + 3) * (5 / 2))",  # Симетрично
    "((2 + 3) * (5 / 2)",  # Несиметрично
    "{[a * (b + c)] - (d / e)}",  # Симетрично
    "{[a * (b + c)] - (d / e)",  # Несиметрично
    "({[}])",  # Несиметрично
    "This is a test string.",  # Симетрично
    "((Here is (some) [text])",  # Несиметрично
    "(({[[]]}))",  # Симетрично
    "Text with unmatched delimiter)",  # Несиметрично
    "(All (brackets) are [properly] {matched})",  # Симетрично
]

for test in test_cases:
    result = check_delimiters(test)
    print(f"{test}: {result}")
